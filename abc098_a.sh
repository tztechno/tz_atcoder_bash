max() {
    if (( $1 < $2 )); then
        expr $2
    else
        expr $1
    fi
}

read a b

add=$(( a + b ))
sub=$(( a - b ))
mul=$(( a * b ))
max_add_sub=$(max $add $sub)
max=$(max $mul $max_add_sub)

echo $max


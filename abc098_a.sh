#!/bin/bash
read a b
ans=$((a+b))
if [ $((a-b)) -ge $ans ]; then
    ans=$((a-b))
fi

if [ $((a*b)) -ge $ans ]; then
    ans=$((a*b))
fi
echo $ans
exit 0
